package com.nike.lifi.entity;

public class PPKBean {
	private String prodCd;

	private String ppk;
	
	private int ppkValue;

	public String getProdCd() {
		return prodCd;
	}

	public void setProdCd(String prodCd) {
		this.prodCd = prodCd;
	}

	public String getPpk() {
		return ppk;
	}

	public void setPpk(String ppk) {
		this.ppk = ppk;
	}

	public int getPpkValue() {
		return ppkValue;
	}

	public void setPpkValue(int ppkValue) {
		this.ppkValue = ppkValue;
	}

}
